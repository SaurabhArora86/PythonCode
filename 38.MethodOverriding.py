class Area:
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def area(self):
        return self.x * self.y


class Circle:
    def __init(self, r):
        super.__init__(r, r)

    def area():
        return 3.14 * super.area()


rec = Area(10, 2)
print(rec.area())

cir = Circle(5)
print(cir.area())
